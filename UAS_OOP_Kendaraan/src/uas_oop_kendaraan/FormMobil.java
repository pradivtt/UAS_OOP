        private void loadData() {
        DefaultTableModel model = (DefaultTableModel) tblMobil.getModel();
        model.setRowCount(0); 

        try {
            java.sql.Connection conn = Koneksi.getConnection();
            java.sql.Statement st = conn.createStatement();
            java.sql.ResultSet rs = st.executeQuery("SELECT * FROM mobil");
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("merk"),
                    rs.getString("warna"),
                    rs.getInt("tahun"),
                    rs.getInt("kecepatan"),
                    rs.getInt("pintu")
                });
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }
    }

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblMobil.getModel();

        selectedRow = tblMobil.getSelectedRow();

        if (selectedRow != -1) {

    try {
        java.sql.Connection conn = Koneksi.getConnection();

        String sql = "DELETE FROM mobil WHERE id=?";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);

        int id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        pst.setInt(1, id);

        pst.executeUpdate();

        model.removeRow(selectedRow);
        selectedRow = -1;

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Delete DB Error: " + e.getMessage());
    }

} else {
    javax.swing.JOptionPane.showMessageDialog(this, "Pilih dulu data di tabel!");
}
    }//GEN-LAST:event_btnHapusActionPerformed


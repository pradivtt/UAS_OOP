    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String merk = txtMerk.getText();
    String warna = txtWarna.getText();
    int tahun = Integer.parseInt(txtTahun.getText());
    int kecepatan = Integer.parseInt(txtKecepatan.getText());
    int pintu = Integer.parseInt(txtPintu.getText());

    DefaultTableModel model = (DefaultTableModel) tblMobil.getModel();

    // =========================
    // UPDATE MODE
    // =========================
    if (selectedRow != -1 && selectedRow < model.getRowCount()) {

        int id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());

        try {
            java.sql.Connection conn = Koneksi.getConnection();

            String sql = "UPDATE mobil SET merk=?, warna=?, tahun=?, kecepatan=?, pintu=? WHERE id=?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, merk);
            pst.setString(2, warna);
            pst.setInt(3, tahun);
            pst.setInt(4, kecepatan);
            pst.setInt(5, pintu);
            pst.setInt(6, id);

            pst.executeUpdate();

            // update tabel UI
            model.setValueAt(merk, selectedRow, 1);
            model.setValueAt(warna, selectedRow, 2);
            model.setValueAt(tahun, selectedRow, 3);
            model.setValueAt(kecepatan, selectedRow, 4);
            model.setValueAt(pintu, selectedRow, 5);

            selectedRow = -1;

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Update DB Error: " + e.getMessage());
        }

    } 
    // =========================
    // INSERT MODE
    // =========================
    else {

        try {
            java.sql.Connection conn = Koneksi.getConnection();

            String sql = "INSERT INTO mobil (merk, warna, tahun, kecepatan, pintu) VALUES (?, ?, ?, ?, ?)";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql, java.sql.Statement.RETURN_GENERATED_KEYS);

            pst.setString(1, merk);
            pst.setString(2, warna);
            pst.setInt(3, tahun);
            pst.setInt(4, kecepatan);
            pst.setInt(5, pintu);

            pst.executeUpdate();

            java.sql.ResultSet rs = pst.getGeneratedKeys();

            int id = 0;
            if (rs.next()) {
                id = rs.getInt(1);
            }

            model.addRow(new Object[]{
                id,
                merk,
                warna,
                tahun,
                kecepatan,
                pintu
            });

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "DB Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

